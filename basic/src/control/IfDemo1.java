package control;

public class IfDemo1 {
	public static void main(String[] args) {
		/*
		 * if (조건식) {
		 * 		조건식이 true로 판정될 때 실행할 수행문;
		 * 		조건식이 true로 판정될 때 실행할 수행문;
		 * }
		 * 
		 * 조건식은 연산 결과가 true/false인 연산식만 가능하다.
		 * 
		 */
		
		int num = 10;
		
		// num 변수에 저장된 값이 0보다 클 때만 실행해야 한다.
		if (num > 0) {
			System.out.println("양의 정수다.");
			
		}
	}

}
